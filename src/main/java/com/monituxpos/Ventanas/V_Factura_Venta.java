    public void cargarItems() {
    
        contenedor.removeAll();
        icono_carga.setVisible(true); // Mostrar 칤cono de carga

    SwingWorker<Void, Void> worker = new SwingWorker<>() {
        @Override
        protected Void doInBackground() {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("MonituxPU");
            EntityManager em = emf.createEntityManager();
            cargar_Items(Secuencial_Empresa, contenedor, contenedor_selector, em);
            em.close();
            emf.close();
            return null;
        }

        @Override
        protected void done() {
            icono_carga.setVisible(false); // Ocultar 칤cono de carga
            revalidate();
            repaint();
        }
    };

    worker.execute(); // Ejecutar en segundo plano
}

    
    
